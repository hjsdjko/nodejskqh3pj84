							this.ruleForm.menpiaofeiyong = obj[o];
							this.ro.menpiaofeiyong = true;
							continue;
						}
						if(o=='jingdianweizhi'){
							this.ruleForm.jingdianweizhi = obj[o];
							this.ro.jingdianweizhi = true;
							continue;
